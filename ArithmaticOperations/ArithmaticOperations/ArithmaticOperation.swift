import Foundation

public class ArithmeticOperations{
    
    public static func add(firstNo: Int, secondNo: Int) -> Int{
        return firstNo + secondNo
    }
    public static func subtract(firstNo: Int, secondNo: Int) -> Int{
        return firstNo - secondNo
    }
    public static func multiply(firstNo: Int, secondNo: Int) ->Int{
        return firstNo * secondNo
    }
    public static func divide(firstNo: Double, secondNo: Double) ->Double{
        return firstNo/secondNo
    }
}
